//	実体
InputManager*	inputManager = nullptr;

//----------------------------------------------------------------------------------------------
//	初期化・解放
//----------------------------------------------------------------------------------------------

	//	コンストラクタ
	InputManager::InputManager( void )
	{
		for ( int p = 0; p < PLAYER_MAX; p++ )
		{
			ZeroMemory( &inputInfo[p], sizeof( InputInfo ) );
		}
	}

	//	デストラクタ
	InputManager::~InputManager( void )
	{

	}

//----------------------------------------------------------------------------------------------
//	更新
//----------------------------------------------------------------------------------------------

//----------------------------------------------------------------------------------------------
//	情報設定
//----------------------------------------------------------------------------------------------

	//	入力情報設定
	void	InputManager::SetInput( int id, int buttonType, int inputType )
	{
		inputInfo[id].buttonType = buttonType;
		inputInfo[id].inputType = inputType;
	}

	//	スティック入力情報せってい
	void	InputManager::SetAxis( int id, float axisX, float axisY )
	{
		inputInfo[id].axisX = axisX;
		inputInfo[id].axisY = axisY;
	}

//----------------------------------------------------------------------------------------------
//	情報取得
//----------------------------------------------------------------------------------------------

	//	入力情報取得
	InputInfo&	InputManager::GetInput( int id )
	{
		return	inputInfo[id];
	}

