//----------------------------------------------------------------------------------------------
//	更新
//----------------------------------------------------------------------------------------------

//----------------------------------------------------------------------------------------------
//	情報設定
//----------------------------------------------------------------------------------------------

	//	モード設定( 新規モードが同じならfalseをかえす )
	bool	BaseChara::SetMode( int nextMode )
	{
		if ( mode != nextMode )
		{ 
			mode = nextMode;
			return	true;
		}
		
		return	false;
	}

//----------------------------------------------------------------------------------------------
//	情報取得
//----------------------------------------------------------------------------------------------

	//	座標取得
	Vector3	BaseChara::GetPos( void )const
	{
		return	pos;
	}
	
