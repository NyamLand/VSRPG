//----------------------------------------------------------------------------------------------
//	初期化・解放
//----------------------------------------------------------------------------------------------

	//	コンストラクタ
	FrameWork::FrameWork( void ) : scene( nullptr )
	{
	
	}

	//	デストラクタ
	FrameWork::~FrameWork( void )
	{
		if ( scene != nullptr )
		{
			delete	scene;
			scene = nullptr;
		}
	}

//----------------------------------------------------------------------------------------------
//	更新
//----------------------------------------------------------------------------------------------

	//	更新
	void	FrameWork::Update( void )
	{
		if ( scene != nullptr )	scene->Update();
	}

//----------------------------------------------------------------------------------------------
//	動作関数
//----------------------------------------------------------------------------------------------

	//	シーン切り替え
	void	FrameWork::ChangeScene( Scene*	newScene )
	{
		//	現在のシーン解放
		if ( scene != nullptr )
		{
			delete	scene;
			scene = nullptr;
		}

		//	シーンの切り替え＆初期化
		scene = newScene;
		scene->Initialize();
	}

