//*****************************************************************************************************************************
//
//		プレイヤークラス
//
//*****************************************************************************************************************************
class cPlayer
{
private:
	//	パラメーた
	int			type;		//	タイプ
	Vector3		pos;		//	座標
	float		angle;		//	方向

public:
	//------------------------------------------------------
	//	初期化・解放
	//------------------------------------------------------
	cPlayer();
	~cPlayer();

	//------------------------------------------------------
	//	更新・描画
	//------------------------------------------------------
	bool Update( PlayerParam& param );

	//------------------------------------------------------
	//	ゲッター・セッター
	//------------------------------------------------------
	Vector3 getPos(){ return pos; }
};


//*****************************************************************************************************************************
//
//		プレイヤー管理クラス
//
//*****************************************************************************************************************************

class cPlayerManager
{
private:
	cPlayer*	player[ PLAYER_MAX ];			//	敵クラス

	GameParam* gameParam;
public:
	//------------------------------------------------------
	//	初期化・解放
	//------------------------------------------------------
	cPlayerManager( GameParam* gameParam );
	~cPlayerManager();

	//------------------------------------------------------
	//	更新・描画
	//------------------------------------------------------
	void Update( int id );

	//------------------------------------------------------
	//	プレイヤー生成
	//------------------------------------------------------
	void Set( int id, int type );

	//------------------------------------------------------
	//	ゲッター・セッター
	//------------------------------------------------------
	Vector3 getPos( int id ){ return player[id]->getPos(); }
};


