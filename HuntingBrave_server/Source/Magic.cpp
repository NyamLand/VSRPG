	//	コンストラクタ
	Magic::Magic( void ) : timer( nullptr ),
		pos( 0.0f, 0.0f, 0.0f ), vec( 0.0f, 0.0f, 0.0f ),
		speed( MAGIC_SPEED ),
		mode( 0 ),
		isHit( false )
	{
	
	}

	//	デストラクタ
	Magic::~Magic( void )
	{
		if ( timer != nullptr )
		{
			delete	timer;	
			timer = nullptr;
		}
	}

	//	初期化
	bool	Magic::Initialize( int id, const Vector3& pos, const Vector3& vec )
	{
		this->pos = pos;
		this->vec = vec;
		this->vec.Normalize();
		timer = new Timer();
		timer->Start( MAGIC_ACTIVE_TIME );

		return	true;
	}

//----------------------------------------------------------------------------------------------
//	更新
//----------------------------------------------------------------------------------------------

	//	更新
	bool	Magic::Update( void )
	{
		//	移動
		Move();

		//	タイマー更新
		bool	ret = !timer->Update();

		if ( ret )	return	true;
		return	true;
	}

