//--------------------------------------------------------------------------------------------
//	グローバル
//--------------------------------------------------------------------------------------------

#define	COUNT_MAX	3

//--------------------------------------------------------------------------------------------
//	初期化・解放
//--------------------------------------------------------------------------------------------

	//	コンストラクタ
	WaitLoad::WaitLoad( void ) : font( nullptr ), timer( nullptr ),
		count( 0 )
	{
		font = new Font( "HG行書体", 50 );
		timer = new Timer();
		sprintf_s( text, "NowLoading" );
		timer->Start( 0.5f );
	}

	//	デストラクタ
	WaitLoad::~WaitLoad( void )
	{
		SafeDelete( font );
		SafeDelete( timer );
	}

//--------------------------------------------------------------------------------------------
//	更新・描画
//--------------------------------------------------------------------------------------------

	//	更新
	void	WaitLoad::Update( void )
	{
		if ( timer->Update() )
		{
			timer->Start( 0.5f );
			strcat( text, "." );

			if ( count == COUNT_MAX )
			{
				count = 0;
				sprintf_s( text, "NowLoading" );
			}
			else	count++;
		}
	}

	//	描画
	void	WaitLoad::Render( void )
	{
		font->DrawFont( text, 900, 650, 1280, 720, 0xFFFFFFFF );
	}

