#define	ITEM_INTERVAL	5.0f

//-------------------------------------------------------------------------------------
//	初期化・解放
//-------------------------------------------------------------------------------------

	//	コンストラクタ
	Item::Item( void ) : timer( nullptr ), itemImage( nullptr ),
		itemState( true ), percentage( 1.0f ) 
	{
		
	}

	//	デストラクタ
	Item::~Item( void )
	{
		SafeDelete( timer );
		SafeDelete( itemImage );
	}

	//	初期化
	bool	Item::Initialize( void )
	{
		timer = new Timer();
		itemImage = new Image();
		//itemImage->Initialize();
		return	true;
	}

//-------------------------------------------------------------------------------------
//	更新・描画
//-------------------------------------------------------------------------------------

	//	更新
	void	Item::Update( void )
	{
		itemState = timer->Update();
		percentage = ( ITEM_INTERVAL - timer->GetRemainingTime() ) / ITEM_INTERVAL;
	}

	//	描画
	void	Item::Render( void )
	{
		
	}

//-------------------------------------------------------------------------------------
//	動作関数
//-------------------------------------------------------------------------------------

	//	アイテム試用
	void	Item::UseItem( void )
	{
		if ( !itemState )		return;
		timer->Start( ITEM_INTERVAL );
		percentage = 0.0f;
	}

