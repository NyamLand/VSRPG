	//	コンストラクタ
	Magic::Magic( void ) : obj( nullptr ),
		pos( 0.0f, 0.0f, 0.0f ), 
		angle( 0.0f )
	{
	
	}

	//	デストラクタ
	Magic::~Magic( void )
	{
		//SafeDelete( obj );
	}

	//	初期化
	void	Magic::Initialize( iexMesh* org, const Vector3& Pos, float Angle )
	{
		this->pos = Pos;
		this->angle = Angle;
		obj = org;
		obj->SetPos( pos );
		obj->SetAngle( angle );
		obj->Update();
	}

//----------------------------------------------------------------------------------------------
//	更新・描画
//----------------------------------------------------------------------------------------------

	//	更新
	void	Magic::Update( void )
	{
		obj->SetPos( pos );
		obj->Update();
	}

	//	描画
	void	Magic::Render( void )
	{
		obj->Render();
	}

//----------------------------------------------------------------------------------------------
//	動作関数
//----------------------------------------------------------------------------------------------

//----------------------------------------------------------------------------------------------
//	情報設定
//----------------------------------------------------------------------------------------------

	//	座標設定
	void	Magic::SetPos( const Vector3& pos )
	{
		this->pos = pos;
	}

	//	メッシュ設定
	void	Magic::SetMesh( iexMesh*	mesh )
	{
		obj = mesh;
	}

