	//	コンストラクタ
	PlayerName::PlayerName( void )
	{
		Initialize();
	}

	//	デストラクタ
	PlayerName::~PlayerName( void )
	{

	}

	//	初期化
	void	PlayerName::Initialize( void )
	{
		for ( int id = 0; id < PLAYER_MAX; id++ )
		{
			ZeroMemory( &nameInfo[id], sizeof( NameInfo ) );
		}
	}

//--------------------------------------------------------------------------------------
//	動作関数
//--------------------------------------------------------------------------------------

	//	インデックスから文字を検索して連結
	char*	PlayerName::SrcCharFromIndex( int* nameIndex )
	{
		char	out[17] = "";
		for ( int i = 0; i < 4; i++ )
		{
			strcat( out, nameChar[nameIndex[i]] );
		}

		return	out;
	}

	//	名前設定
	void		PlayerName::SetName( int id, int* nameIndex )
	{
		char*	name = SrcCharFromIndex( nameIndex );
		strcpy( nameInfo[id].name, name );

		for ( int i = 0; i < 4; i++ )
		{
			nameInfo[id].nameIndex[i] = nameIndex[i];
		}
	}

//--------------------------------------------------------------------------------------
//	情報取得
//--------------------------------------------------------------------------------------

	//	インデックス取得
	int*	PlayerName::GetNameIndex( int id )
	{
		return	nameInfo[id].nameIndex;
	}

	//	名前取得
	char*	PlayerName::GetName( int id )
	{
		return	nameInfo[id].name;
	}

