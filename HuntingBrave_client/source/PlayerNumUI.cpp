	//	コンストラクタ
	PlayerNumUI::PlayerNumUI( int player, int x, int y, int w, int h )
	{
		numImage = new Image();
		numImage->Initialize("DATA/UI/main_UI/PlIcon.png",
			x, y - 10, w, h, 0, player * 64, 64, 64 );
	}

	//	デストラクタ
	PlayerNumUI::~PlayerNumUI( void )
	{
		SafeDelete( numImage );
	}

//---------------------------------------------------------------------------------------
//	更新・描画
//---------------------------------------------------------------------------------------

	//	更新
	void	PlayerNumUI::Update( int player )
	{
		numImage->sy = pointManager->GetRank(player) * 128;
	}

	//	更新
	void	PlayerNumUI::Update( int x, int y, int w, int h )
	{
		numImage->x = x;
		numImage->y = y;
		numImage->w = w;
		numImage->h = h;
	}

	//	描画
	void	PlayerNumUI::Render( void )
	{
		numImage->Render(IMAGE_MODE::NORMAL);
	}

