//	コンストラクタ
ItemUI::ItemUI(int x, int y, int w, int h)
{
	//	座標、サイズ情報格納
	width = w / 2;
	height = h;

	//		左端			画像の中心
	posx = (x - w / 2) + (width / 2);
	posy = y -(h / 2) - (height / 2);

	obj = new Image();
	obj->Initialize("DATA/UI/main_UI/HP_item_UI.png", posx, posy, width, height, ITEM_MAX::SPX, ITEM_MAX::SPY, ITEM_MAX::WIDTH, ITEM_MAX::HEIGHT);

}

//	デストラクタ
ItemUI::~ItemUI(void)
{
	SafeDelete(obj);
}



