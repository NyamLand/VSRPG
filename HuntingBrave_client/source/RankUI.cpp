	//	コンストラクタ
	RankUI::RankUI( int x, int y, int w, int h )
	{
		rankImage = new Image();
		rankImage->Initialize( "DATA/UI/main_UI/Number.png",
			x, y, w, h, 0, 384, 128, 64 );
	}

	//	デストラクタ
	RankUI::~RankUI( void )
	{
		SafeDelete( rankImage );
	}

//---------------------------------------------------------------------------------------
//	更新・描画
//---------------------------------------------------------------------------------------

	//	更新
	void	RankUI::Update( int player )
	{
		rankImage->sx = pointManager->GetRank( player ) * 128;
	}

	//	更新
	void	RankUI::Update( int x, int y, int w, int h )
	{
		rankImage->x = x;
		rankImage->y = y;
		rankImage->w = w;
		rankImage->h = h;
	}

	//	描画
	void	RankUI::Render( void )
	{
		rankImage->Render( IMAGE_MODE::NORMAL );
	}

